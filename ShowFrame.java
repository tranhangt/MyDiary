    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblDiary.getSelectedRow();
        if(selectedIndex >= 0){
            Diary dia = diaryList.get(selectedIndex);
            ShowContentFrame showContentFrame = new ShowContentFrame();
            showContentFrame.setTextforLb(dia);

            showContentFrame.setVisible(true);
            this.dispose();
            
        }
        
        
    }//GEN-LAST:event_btnShowActionPerformed

