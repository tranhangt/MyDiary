    private void showDiary(){
        diaryList = DiaryModify.findAll();
        
        tableModel.setRowCount(0);
        
        diaryList.forEach(diary -> {
            tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, diary.getDate(), diary.getDescription()});
        });
    }

